# 9. Create a string and print the characters from 5th to 10 in direct and reverse order.

def print_characters():
    string = "VipinKumar"
    print("String: ", string)
    print("Characters from 5th to 10 in direct order: ", string[4:10])
    print("Characters from 5th to 10 in reverse order: ", string[9:3:-1])
print_characters()